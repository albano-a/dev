local opt = vim.opt

opt.number = true
opt.relativenumber = true
opt.cursorline = true -- highlights line
opt.scrolloff = 8 -- keep margin of 8 lines
opt.signcolumn = "yes" -- always shows sign column

opt.tabstop = 4 -- tab width in spaces
opt.shiftwidth = 4 -- indentation width
opt.expandtab = true -- converts tab into spaces
opt.smartindent = true

opt.wrap = false
opt.termguicolors = true

